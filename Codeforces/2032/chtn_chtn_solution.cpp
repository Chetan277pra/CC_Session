#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int tt;
    cin >> tt;

    while (tt--) {
        int n;
        cin >> n;

        vector<long long> vec(n);
        for (auto &x : vec) cin >> x;

        sort(vec.begin(), vec.end());

        long long ans = n;
        for (int i = 0; i + 1 < n; i++) {
            long long sum = vec[i] + vec[i + 1];
            auto it = lower_bound(vec.begin(), vec.end(), sum);
            long long p = vec.end() - it;
            ans = min(ans, (long long)i + p);
        }

        cout << ans << "\n";
    }

    return 0;
}
