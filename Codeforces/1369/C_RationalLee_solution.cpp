#include <bits/stdc++.h>
using namespace std;
#define int long long
int32_t main()
{
    int tt;
    cin >> tt;
    while(tt--){
        int n , k;
        cin >> n >> k;
        vector<int> vec(n);
        for(auto & a : vec) cin >> a;
        sort(vec.begin() , vec.end());
        int tot = 0;
        vector<int> fry(k);
        for(auto & a : fry) cin >> a;
        sort(fry.begin() , fry.end());
        vector<int> temp;
        for(auto a : fry) if(a != 1) temp.push_back(a);
        int l = 0, h = n-1;
        for(auto a : fry){
            if(a == 1){
                tot += 2*(vec[h--]);
            }
            else
                break;
        }
        sort(temp.rbegin() , temp.rend());
        for(auto a : temp){
            tot+=(vec[h--]);
            tot += vec[l++];
            a -= 2;
            while(a > 0) {l++ ; a--;}
        }
        cout<<tot<<"\n";
    }
    return 0;
}