#include <bits/stdc++.h>
using namespace std;

const int MAXN = 200005;

void solve() {
    int n;
    cin >> n;
    vector<int> arr(n + 1), idx(n + 1);
    vector<long long> res(n + 1);
    long long cur = 0;

    for (int i = 1; i <= n; i++) {
        cin >> arr[i];
        idx[i] = i;
    }

    sort(idx.begin() + 1, idx.begin() + n + 1, [&](int a, int b) {
        return arr[a] < arr[b];
    });

    cur = n;
    for (int i = 1; i < n; i++) cur += 1LL * (n - i) * (arr[idx[i + 1]] - arr[idx[i]]);

    for (int i = 1; i <= n; i++) {
        res[idx[i]] = cur;
        if (i == n) break;
        cur -= 1LL * (n - i) * (arr[idx[i + 1]] - arr[idx[i]]);
        cur += 1LL * i * (arr[idx[i + 1]] - arr[idx[i]]);
    }

    for (int i = 1; i <= n; i++) {
        cout << res[i] << (i == n ? '\n' : ' ');
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--) solve();
}
